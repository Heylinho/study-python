n1 = 20
n2 = 3 

#Adição
res = n1 + n2
print(f"{n1} + {n2} = {res}")

#Subtração
res = n1 - n2
print(f"{n1} - {n2} = {res}")

#Multiplicação
res = n1 * n2
print(f"{n1} * {n2} = {res}")

#Divisão
res = n1 / n2
print(f"{n1} / {n2} = {res:.2f}")#Configuração de quantas casas depois da virgula

#Numero inteiro -Quantas vezes cabe o numero 2 no numero 1
res = n1 // n2
print(f"{n1} // {n2} = {res}")

#Resto da divisão
res = n1 % n2
print(f"{n1} % {n2} = {res}")

#Potencia
res = n1 ** n2
print(f"{n1} ** {n2} = {res}")

#n1 = n1 + 10
n1 += 10
print(f"n1 = {n1}")